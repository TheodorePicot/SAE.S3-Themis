        <h1>Comment fonctionne le site ?</h1>
    </div>
        <div class="row mx-5">


            <p>Consultation des questions : Une fois inscrit, vous pouvez consulter les questions en cours sur la page
                d'accueil. Vous pouvez également utiliser la barre de recherche pour rechercher des questions
                spécifiques.</p>

            <p>Organisation des questions : Si vous avez été désigné comme organisateur, vous pouvez organiser des
                questions en les créant ou en les modifiant. Vous pouvez également choisir les auteurs des propositions
                pour chaque question.</p>

            <p>Rédaction des propositions : Si vous avez été désigné comme auteur de proposition, vous pouvez rédiger
                des propositions pour les questions en cours. Assurez-vous de respecter les consignes de longueur et de
                contenu pour chaque proposition.</p>

            <p>Votes : Pendant la période de vote, les utilisateurs qui ont été désignés comme votants peuvent voter
                pour leur proposition préférée en cliquant sur le bouton "voter" à côté de chaque proposition.</p>

            <p>Résultats : Les résultats des votes sont affichés une fois la période de vote terminée. Les utilisateurs
                peuvent consulter les résultats sur la page de résultat</p>
